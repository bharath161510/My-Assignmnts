package week2.day1;


public class LearnForLoop {

	public static void main(String[] args) {
		
//pseudocode
		//create for loop 
		// give i =10 and iterate values from 10 to 1 

		for (int i = 1; i <=5; i++) {
			System.out.println(i);
		}
		
		for (int i = 10; i >=0; i--) {
			System.out.print(i+" ");
			
		}
	}

}
