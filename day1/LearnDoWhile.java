package week2.day1;

public class LearnDoWhile {

//psduocode
	//Learning DO While
	//In DO while execution will happen statement will be printed.
	//Then it will check the condition and execute remaining
	public static void main(String[] args) {
		int a= 10;
		
		do {
			 System.out.println(a);
			 a--;
		}
		while (a>=0);
	}

}
